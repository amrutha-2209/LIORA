package day5;

public class LoopingStatement {

	public static void main(String[] args) {
		for(int i=146;i<=150;i++)
		{
			System.out.print(i+" ");
		}
		System.out.println("\n");
		
		
		for(int i=5;i>=1;i--)
		{
			System.out.print(i+" ");
		}
		System.out.println("\n");
		
		for(int i=2;i<=10;i+=2)
		{
			System.out.print(i+" ");
		}
		System.out.println("\n");
		
		for(int i=1;i<=9;i+=2)
		{
			System.out.print(i+" ");
		}

	}

}
